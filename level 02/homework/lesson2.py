num3 = int(input('შეიყვანე მესამე რიცხვი:'))
print("+:",num1 + num2 + num3)
print("-:",num1 - num2 - num3)
print("*:",num1 * num2 * num3)
print("/:",num1 / num2 / num3)
print("**:",num1 ** num2 ** num3)
print("//:",num1 // num2 // num3)







