# 4) შემოაყვანინეთ მომხმარებელს 3 რიცხვი, შემდგომ გამოიტანეთ ტერმინალში ამ რიცხვებს შორის ყველა მათემატიკური ოპერაციის შედეგი.
num1 = int(input("შეიყვანე პირველი რიცხვი:"))
